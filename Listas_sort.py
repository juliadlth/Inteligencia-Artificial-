#Ordena la siguiente lista en orden alfabético descendente (de la letra 'z' a la 'a').
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón',
          'lila', 'negro', 'rosa', 'blanco', 'naranja']      #Declaro la lista y sus elementos 
colores.sort(reverse=True)     #Declaro la lista colores con 'sort(reverse=True)'para que los elementos se ordenen en orden alfabetico pero empezando de la 'z'
print(colores)                 #Imprimo la lista ya ordenada 
