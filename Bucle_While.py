#Crea un bucle while que se ejecute hasta que x valga 15 con incrementos de 5.
x=0 #Establezco mi valor de x 
while x <= 15: #Creo el bucle donde se cumplira la condicion donde x sea menor o igual a 15
    print(x) #Imprimo mi bucle 
    x+=5      #Incremento mi x de 5 en 5 hasta llegar a 15 
    
print("\n") #Doy un salto de linea

