#Convierte la siguiente lista en una tupla y asegúrate que se haya convertido en tupla correctamente
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja'] #Declaro la lista y sus elementos 
tupla=tuple(colores)  #Transformo mi lista colores a una tupla por medio de 'tuple()'
print(tupla)          #Imprimo mi tupla y ya han cambiado los corchetes a parentesis
print("\n",type(tupla)) #confirmo por medio de 'type' para ver de que clase es 
